"""
Internationalization module for Streampeg.

Supported languages: en, de, fr, es, pl, it
Default: en
"""

from db import get_setting, set_setting

LANGUAGES = {
    "en": "English",
    "de": "Deutsch",
    "fr": "Français",
    "es": "Español",
    "pl": "Polski",
    "it": "Italiano",
}

DEFAULT_LANG = "en"


def get_language():
    val = get_setting("ui_language")
    return val if val in LANGUAGES else DEFAULT_LANG


def set_language(lang):
    if lang in LANGUAGES:
        set_setting("ui_language", lang)


def t(key, lang=None):
    """Translate a key. Falls back to English, then returns the key itself."""
    if not lang:
        lang = get_language()
    tr = TRANSLATIONS.get(lang, TRANSLATIONS[DEFAULT_LANG])
    return tr.get(key, TRANSLATIONS[DEFAULT_LANG].get(key, key))


def get_all_translations(lang=None):
    """Return full translation dict for current language (for JS)."""
    if not lang:
        lang = get_language()
    base = dict(TRANSLATIONS[DEFAULT_LANG])
    if lang != DEFAULT_LANG:
        base.update(TRANSLATIONS.get(lang, {}))
    return base


